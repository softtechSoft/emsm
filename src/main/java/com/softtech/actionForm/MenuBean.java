package com.softtech.actionForm;

public class MenuBean {
	String functionText;
	String functionLink;
	String displayNo;
	/**
	 * @return functionText
	 */
	public String getFunctionText() {
		return functionText;
	}
	/**
	 * @param functionText セットする functionText
	 */
	public void setFunctionText(String functionText) {
		this.functionText = functionText;
	}
	/**
	 * @return functionLink
	 */
	public String getFunctionLink() {
		return functionLink;
	}
	/**
	 * @param functionLink セットする functionLink
	 */
	public void setFunctionLink(String functionLink) {
		this.functionLink = functionLink;
	}
	/**
	 * @return displayNo
	 */
	public String getDisplayNo() {
		return displayNo;
	}
	/**
	 * @param displayNo セットする displayNo
	 */
	public void setDisplayNo(String displayNo) {
		this.displayNo = displayNo;
	}
}


